class UsersController < ApplicationController
  before_filter :authorize, except: [:new , :create, :signin, :login]  
  before_action :set_user, only: [:show, :edit, :update, :destroy]

  # GET /users
  # GET /users.json
  def index
  
    @users = User.all
  end

  # GET /users/1
